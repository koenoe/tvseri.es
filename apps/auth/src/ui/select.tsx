export interface SelectUIOptions {
  providers?: Record<
    string,
    {
      hide?: boolean;
      display?: string;
    }
  >;
}

export function SelectUI(options?: SelectUIOptions) {
  return async (
    providers: Record<string, string>,
    _req: Request,
  ): Promise<Response> => {
    const jsx = (
      <Layout>
        <div data-component="form">
          {Object.entries(providers).map(([key, type]) => {
            const match = options?.providers?.[key];
            if (match?.hide) {
              return null;
            }

            const icon = ICON[key];
            return (
              <a
                data-color="ghost"
                data-component="button"
                href={`/${key}/authorize`}
              >
                {icon && <span data-slot="icon">{icon}</span>}
                <span data-slot="text">
                  Continue with {match?.display ?? DISPLAY[type] ?? type}
                </span>
              </a>
            );
          })}
        </div>
      </Layout>
    );

    return new Response(jsx.toString(), {
      headers: {
        'Content-Type': 'text/html',
      },
    });
  };
}
